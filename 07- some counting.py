#Adding 1 to a series starting at 0 and ending at 50.
print("counting up from 0 to 50:")
for num in range(0, 51):
    print (num)

#substracting 1 to a series starting at 50 and ending at 0.
print("\ncounting down from 50 to 0:")
for num in range(50, -1, -1):
    print(num)

#Adding 1 to a series starting at 30 and ending at 50.
print("\ncounting up from 30 to 50:")
for num in range(30, 51):
    print(num)

#substracting 2 to a series starting at 50 and ending at 10.
print("counting down from 50 to 10 in decrements of 2:")
for num in range(50, 9, -2):
    print(num)

#Adding 5 to a series starting at 100 and ending at 200.
print("\ncounting up from 100 to 200 in increments of 5:")
